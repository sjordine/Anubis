package br.unicamp.ic.anubis.event;

import java.util.UUID;

public class AlignmentDataLoadedEvent extends DataLoadedEvent {
	
	
	private int alignmentIndex;
	
	
	public AlignmentDataLoadedEvent(UUID source,int alignmentIndex){
		super(source);
		this.alignmentIndex = alignmentIndex;
	}
	

	public int getAlignmentIndex() {
		return alignmentIndex;
	}

}
