package br.unicamp.ic.anubis.event;

import br.unicamp.ic.anubis.mechanism.messaging.IEvent;

public class RowSelectedEvent implements IEvent {
	
	private int row;
	private int alignmentId;
	
	public RowSelectedEvent(int alignmentId, int row){
		this.row = row;
		this.alignmentId = alignmentId;
	}

	public int getRow() {
		return row;
	}

	public int getAlignmentId() {
		return alignmentId;
	}

}
