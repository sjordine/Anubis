package br.unicamp.ic.anubis.event;

import java.awt.Component;
import java.awt.Point;

public class AlignmentRowPopUpMenuEvent extends AlignmentShowPopUpMenuEvent {
	
	private int selectedRow;

	public AlignmentRowPopUpMenuEvent(int alignmentId, int selectedRow, Point point, Component container) {
		super(alignmentId, point, container);
		this.selectedRow = selectedRow;
	}

	public int getSelectedRow() {
		return selectedRow;
	}

}
